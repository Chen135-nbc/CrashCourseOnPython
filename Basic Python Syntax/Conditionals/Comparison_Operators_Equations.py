# PART 1: Equality == and Not Equal To != Operators

print(32 == 30+2) #return True
print(10+8 == 9+7) #return False
print(10-4 != 10+4)
print(9/3 != 3*1)

# The equality == operator versus the equals = operator
# equals operator "="
my_variable = 3 * 5
print(my_variable)
# equality operator "=="
print(my_variable == 3*5)

# PART 2: Greater Than > and Less Than < Operators
# > Greater Than
print(11 > 3*3)  #True
print(4/2 > 8-4) #False

# < Less Than
print(4/2 < 8/4) #False
print(11 < 3*2) #False

# PART 3: Greater Than or Equal to >= and Less Than or Equal to <= Operators
# Greater Than or Equal to >=
print(12*2 >= 24)
print(18/2 >= 15)
# Less Than or Equal to <=
print(12*2 <= 30)
print(15 <= 18/2)