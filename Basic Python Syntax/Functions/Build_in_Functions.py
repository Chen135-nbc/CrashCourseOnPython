# Build-in function

# print()
month = "November"
print("This month is ",month)

# type()
print(type(month))
print(type(355))
print(type(256.77))

# str()
day = 30
print("There is " + str(day) + " days in a month.")

# sorted()
time_list = [12,2,44,67,35,23,7,23]
print(time_list)
print(sorted(time_list))

# max() and min()
a = max(time_list)
b = min(time_list)
print("The Max number is " + str(a))
print("The Min number is " + str(b))
